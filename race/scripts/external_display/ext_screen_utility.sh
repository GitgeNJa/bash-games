print_centered_text()
{
    text=$1
    speed=$2
    for ((i = 0; i < ${#text}; i++)); do
        echo -n "${text:$i:1}"
        if [ "${speed}" -eq 1 ]; then
            sleep 0.05
        elif [ "${speed}" -eq 2 ]; then
            sleep 0.1
        elif [ "${speed}" -eq 3 ]; then
            sleep 0.5
        fi
    done
}

